  # result in loss.                                                           #
  #############################################################################
  num_train = X.shape[0]
  num_classes = W.shape[1]
  scores = X.dot(W)
  correct_class_score = np.reshape(np.repeat(scores[range(num_train),y],num_classes),scores.shape)
  loss = (np.sum(np.maximum(0,scores - correct_class_score + 1)) - num_train) / num_train
  loss += reg * np.sum(W * W)
  #############################################################################
